package com.rpa.response;

public class CarPolicyStatus {
	private String carType;
	private String policyDownloaded;
	private String proposalDownloaded;
	private String policyUploaded;
	private String proposalUploaded;
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public String getPolicyDownloaded() {
		return policyDownloaded;
	}
	public void setPolicyDownloaded(String policyDownloaded) {
		this.policyDownloaded = policyDownloaded;
	}
	public String getProposalDownloaded() {
		return proposalDownloaded;
	}
	public void setProposalDownloaded(String proposalDownloaded) {
		this.proposalDownloaded = proposalDownloaded;
	}
	public String getPolicyUploaded() {
		return policyUploaded;
	}
	public void setPolicyUploaded(String policyUploaded) {
		this.policyUploaded = policyUploaded;
	}
	public String getProposalUploaded() {
		return proposalUploaded;
	}
	public void setProposalUploaded(String proposalUploaded) {
		this.proposalUploaded = proposalUploaded;
	}
	

}
